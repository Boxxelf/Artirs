export const getMentorsByCategory = (category) => {
  if (category === 'all') return mentors;
  return mentors.filter(m => {
    if (m.category) return m.category === category;
    // 根据specialties匹配
    const categoryMap = {
      illustration: ['Digital Illustration', 'Character Design', 'Storytelling', 'Concept Art'],
      product: ['Product Design', 'UX/UI Design', 'Interactive Media', 'Design Systems'],
      architecture: ['Architecture', 'Urban Design', 'Sustainable Design', '3D Modeling'],
      graphic: ['Graphic Design', 'Branding', 'Typography', 'Editorial Design'],
      animation: ['Animation', 'Character Design', 'Storyboarding', '2D Animation'],
    };
    const specialties = categoryMap[category] || [];
    return specialties.some(s => m.specialties.includes(s));
  });
};